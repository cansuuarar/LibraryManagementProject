import Foundation

enum BookType : String, CaseIterable {
    case selfHelp = "Self Help"
    case crime = "Crime"
    case romance = "Romance"
    case novel = "Novel"
}

// case iterable: A type that provides a collection of all of its values., tüm case ler içerisinde dönmemi sağlıyor.
